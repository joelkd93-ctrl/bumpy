/**
 * Get a random message from the baby for a specific week
 * @param {number} week 
 */
export function getBabyMessage(week) {
    const weekClamped = Math.min(40, Math.max(20, week));
    const messages = BABY_MESSAGES[weekClamped] || BABY_MESSAGES[20];

    // Use dev override if available, otherwise current date
    let now = new Date();
    const override = localStorage.getItem('bumpy:dev_date_override');
    if (override) {
        now = new Date(override);
    }

    const dayOfYear = Math.floor(now.getTime() / (1000 * 60 * 60 * 24));
    // Use day of year to keep the same message for the whole day
    return messages[dayOfYear % messages.length];
}
